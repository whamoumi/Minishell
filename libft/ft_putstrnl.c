#include "libft.h"

void	ft_putstrnl(const char *str)
{
	int	i;

	if (str)
	{
		i = 0;
		while (str[i])
		{
			write(1, &str[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
